package net.laserdiamond.intothevoid.util;

/**
 * Class that contains all basic colors and font styles for text
 */
public class TextColor {

    /**
     * Obfuscated text
     */
    public static final String OBFUSCATED = "§k";

    /**
     * Bold text
     */
    public static final String BOLD = "§l";

    /**
     * Strike through text
     */
    public static final String STRIKE_THROUGH = "§m";

    /**
     * Underline text
     */
    public static final String UNDERLINE = "§n";

    /**
     * Italic text
     */
    public static final String ITALIC = "§o";

    /**
     * Resets the color and font style back to the default for proceeding characters
     */
    public static final String RESET = "§r";

    /**
     * Black text
     */
    public static final String BLACK = "§0";

    /**
     * Dark blue text
     */
    public static final String DARK_BLUE = "§1";

    /**
     * Dark green text
     */
    public static final String DARK_GREEN = "§2";

    /**
     * Dark aqua text
     */
    public static final String DARK_AQUA = "§3";

    /**
     * Dark red text
     */
    public static final String DARK_RED = "§4";

    /**
     * Dark purple text
     */
    public static final String DARK_PURPLE = "§5";

    /**
     * Gold text
     */
    public static final String GOLD = "§6";

    /**
     * Gray text
     */
    public static final String GRAY = "§7";

    /**
     * Dark gray text
     */
    public static final String DARK_GRAY = "§8";

    /**
     * Blue text
     */
    public static final String BLUE = "§9";

    /**
     * Green text
     */
    public static final String GREEN = "§a";

    /**
     * Aqua text
     */
    public static final String AQUA = "§b";

    /**
     * Red text
     */
    public static final String RED = "§c";

    /**
     * Light purple text
     */
    public static final String LIGHT_PURPLE = "§d";

    /**
     * Yellow text
     */
    public static final String YELLOW = "§e";

    /**
     * White text
     */
    public static final String WHITE = "§f";
}
